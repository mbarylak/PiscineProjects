#include <unistd.h>

void	ft_is_negative(int n)
{	
	char	sgn;

	if (n >= 0)
	{
		sgn = 'P';
		write(1, &sgn, 1);
	}
	else
	{
		sgn = 'N';
		write(1, &sgn, 1);
	}
}
